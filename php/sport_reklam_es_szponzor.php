					<h4>EAST-WEST SZPONZORÁCIÓS CSOMAG</h4>
					<br />
					<strong>A szponzoráció – partnerség</strong><br />
					<br />
					Szeretné elérni, hogy cége milliók szemében a sport hősei mellé kerülhessen? Szeretné, ha
					tényleg megvalósulna vásárlói felé a minőségi kommunikáció? Ajánlatunkkal Ön és vállalata
					egy olyan klubba nyer meghívást, amelyen keresztül Ön nem csak egy elsőosztályú csapat
					támogatójává válik, de jobban kiaknázhatja marketingtevékenységét, növelheti márkái
					ismertségét és erősítheti cége PR-tevékenységét. A sport támogatásával ráadásul cége
					megítélése jóval kedvezőbb lesz nem csak Magyarországon, de akár jóval határainkon túl is.
					Tekintse át ajánlatunkat, és keressen bennünket!<br />
					<br />
					<strong>Cégbemutatkozás</strong><br />
					<br />
					Az East-West Sport Kft. neve a legmagasabb minőséget jelenti a magyar sportéletben.
					Cégünk 1997-ben Hungasport néven kezdte meg működését, majd 12 év folyamatos fejlesztést
					követően 2009-ben lett új nevünk az East-West Sport, jelképezendő a széles spektrumot, melyet
					cégünk tevékenysége felölel. Először labdarúgó csapatok magyar és külföldi edzőtáborainak
					teljes körű szervezésével foglalkoztunk, majd más sportágak csapatainak és versenyzőinek is
					segítettük professzionális felkészülését.<br />
					Irodánk ma már a sport minden szegmensében szerepet vállal. Fő tevékenységünk továbbra is
					az edzőtáborok szervezése Magyarországon és külföldön, de foglalkozunk reklámfelületek értékesítésével,
					szponzorok közvetítésével különböző klubokhoz és sportszervezetekhez.<br />
					Tevékenységi körünkbe tartozik még játékosok, edzők, gyúrók és orvosok menedzselése,
					magyarországi és külföldi munkalehetőségek közvetítése.<br />
					<br />
					<strong>Cégünk FIFA licensszel is rendelkezik.</strong><br />
					<br />
					Az East-West Sport megbízható partnere a klubcsapatoknak, a vállalatoknak és a futball
					rajongóinak egyaránt. Célunk, hogy közreműködésünkkel a magyar labdarúgócsapatok
					támogatói olyan szakmai alapokon nyugvó, professzionális együttműködésekkel gazdagodjanak,
					amelyek nem csupán a szponzoráló céget, de a klubcsapatokat is sikerre viszik.<br />
					<br />
					<strong>A szponzoráció – érték</strong><br />
					<br />
					A sport az életünk része: életmód és szórakozás egyben. A sporteseményeket sokan
					látogatják, továbbá a televíziós közvetítések révén még többen láthatják, tehát meglehetősen
					széles a célcsoportja. A sport olyan értékeket hordoz mint a csapatszellem, a győzniakarás,
					a kitartás és az ellenfél tisztelete. A futball, mint az egyik legnépszerűbb sportág, ezeket az
					értékeket a legmagasabb fokon, a legnagyobb tömegek előtt képviseli. A sportot támogató
					cégek ezeket az értékeket nem csupán átöröklik – formálják is.<br />
					A sport az élet minden területével összefügg, közösségformáló hatása miatt önmagában társadalmi
					ügy. A sport támogatása, az üzleti szféra társadalmi szerepvállalása elengedhetetlen. Fontos,
					hogy a vállalatok jövedelmük egy részét arra a közösségre fordítsák melyben tevékenykednek,
					és ez egyben a fogyasztók elvárása is. Az Ön cégének társadalmi szerepvállalása nagyobb értéket,
					ismertséget eredményez, ez pedig visszahat a cég gazdasági eredményességére.<br />
					A szponzoráció a szponzorcég alkalmazottai számára is üzenetet hordoz, hiszen ha a támogatott
					csapat eredményt ér el akkor az számukra büszkeséget jelent; ráadásul a támogatással a csapat
					jellemzően gazdaságilag is versenyképesebb lesz, így valószínűbb, hogy még nagyobb sikereket
					fog elérni.<br />
					<br />
					<strong>A szponzoráció – lehetőség</strong><br />
					<br />
					A szponzoráció nem kereskedelmi szituációban szólítja meg a közönséget. A szabadidő
					hasznos eltöltése egyre fontosabb az emberek életében, a szponzorálás segítségével a vállalat
					beépülhet ebbe a környezetbe.<br />
					Egy csapat támogatása ugyanis mindig egy közösség támogatását is jelenti. Egy jól definiálható
					befogadó közeget, amelyen keresztül a támogató egy körforgás részévé válik, melyben a csapat,
					a szurkoló, és a csapatot támogató cég értéket kap és teremt. Ennek feltétele egy, a felek között
					fennálló partneri viszony és egy megalapozott stratégia.<br />
					A támogatás által cége biztosíthatja egy csapat részére, hogy a befektetett kemény munka és a
					kitartás mellett sikereket tudjon elérni, a szurkolók részére megteremtheti a közös öröm, a felejthetetlen
					pillanatok lehetőségét oly módon hogy mindez márkájának erősödésével jár.<br />
					<br />
					<br />
					<h4>Miért érdemes egy csapatot támogatni?</h4>
					A sportbeli invesztíciónak kommunikációs és kereskedelmi előnyei egyaránt vannak. A szponzoráció
					közép- és hosszútávú befektetés, melynek segítségével az általánosan használt marketing
					és kommunikációs eszközök mellett az alábbi célok is megvalósíthatóak:<br />
					<br />
					<strong>- márkaismertség növelése<br />
					- a cég imidzsének erősítése<br />
					- fogyasztói kapcsolatok mélyítése, különleges célpiacokra való belépés lehetősége<br />
					- kereskedelmi, értékesítési lehetőségek<br />
					- üzleti partnerek és munkatársak motiválása<br />
					- társadalmi felelősségvállalás kihangsúlyozása /CSR/<br />
					- kapcsolatépítés: public relations és hospitality lehetőségek</strong><br />
					<br />
					A szponzoráció segítségével sikerülhet a vállalatnak megfognia azokat az érzelmi szálakat, amelyek
					a célcsoportja számára fontos és érdekes, ezen felül a szponzorálás lehetőséget nyújt a
					reklámkorlátozások és -tilalmak legális áthidalására.<br />
					<br />
					<strong>A szponzoráció – befektetés</strong><br />
					<br />
					Az új adótörvények komoly lendületet adhatnak az öt kiemelt csapatsportág – labdarúgás, kézilabda,
					kosárlabda, vízilabda, valamint a jégkorong – számára. Az egyes adótörvényeknek a
					sport támogatásával összefüggő módosításáról szóló törvényjavaslatnak nevezett tervezet szerint
					a vállalkozások társaságiadó-kedvezményt vehetnek igénybe a látványsportágak támogatása
					után. A számítások szerint száz egység támogatásért cserébe a támogató cég a hatályos társasági
					adókulcsokat alapul véve 110-119 egység adókedvezményt vehet igénybe aszerint, hogy
					társasági adókulcsa a tíz és tizenkilenc százalékos társasági adókulcsok szerint miként alakul.<br />
					<br />
					<strong>A közönség</strong><br />
					<br />
					A labdarúgás évtizedek óta a legnépszerűbb sport Magyarországon, egyben a világ legnépszerűbb
					csapatsportja. Az egyes mérkőzések átlagos nézőszáma 4.500-5000 fő, míg nemzetközi
					ligamérkőzések illetve a válogatott mérkőzéseinek esetében ez a szám a többszörösére nőhet.
					A futballszezonban a mérkőzések 70 százalékát közvetíti valamelyik televízió, ez a legnagyobb
					arány a magyar sportágak között. A közvetítő csatornák jellemzően a sportcsatornák (Sport TV:
					Sport1, Sport2, Sport M, valamint DigiSport, EuroSport, EuroSport2, SportKlub) illetve a tv2
					kereskedelmi televízió, ahol az előbbieknél átlagosan mérkőzésenként 6-700.000 nézővel, míg a
					tv2 esetén 1-1,5 millió nézővel lehet számolni. A Sport TV-vel együttműködve bizonyos válogatott
					és nemzetközi mérkőzéseket közvetít még a Duna TV, amely hivatalos közleménye szerint a
					labdarúgás közvetítése bármely más sportágnál négyszer több nézőt hoz a csatornának. A Sport
					TV fordulónként 4 mérkőzést közvetít, ez a 2010-2011-es szezonban összesen 150 magyar élvonalbeli
					mérkőzést jelent, alkalmanként 30-30 perces felvezetéssel, és egy kétórás vasárnapi
					összefoglalóval.<br />
